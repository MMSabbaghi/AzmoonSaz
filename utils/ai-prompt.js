function getAIPrompt(topic, count) {
  return `لطفاً دقیقا ${count} سوال ریاضی با موضوع "${topic}" تولید کن.

**الزامات خروجی فوق‌حرفه‌ای:**

1. **هر آیتم باید یک سوال واقعی و قابل حل باشد.**  
   - هیچ توضیح، تعریف درس، نکته آموزشی یا متن اضافه نباید تولید شود.  
2. خروجی باید **فقط** یک شیء JSON معتبر باشد.  
3. ساختار JSON:
{
  "items": [
    { "text": "متن سوال" }
  ]
}
4. تعداد آیتم‌ها دقیقا ${count} باشد.  
5. هر آیتم فقط یک کلید "text" داشته باشد.  
6. فرمول‌ها:
   - $...$ برای inline
   - $$...$$ برای display
7. **Escape کردن بک‌اسلش‌ها**:
   - هر دستور LaTeX که بک‌اسلش دارد، دو بار escape شود.
   - مثال‌ها:
     - \frac{1}{2} → \\\\frac{1}{2}
     - \sqrt{x} → \\\\sqrt{x}
     - \dots → \\\\dots
8. کاراکترهای خاص (^, _, {, }, (, )) نیازی به escape ندارند مگر در رشته JSON.  
9. هیچ کاما یا کاراکتر اضافه در انتهای آرایه نباشد.  
10. خروجی **مستقیما قابل parse با JSON.parse باشد.**  
11. هیچ متن، توضیح، Markdown یا تعریف درس اضافه ننویس. فقط سوال‌ها.  
12. بررسی نهایی قبل از ارسال:
    - تمام بک‌اسلش‌ها دوبل شده باشند.
    - JSON معتبر باشد.
    - همه سوال‌ها قابل حل باشند و KaTeX-ready باشند.

**نمونه خروجی معتبر:**
{
  "items": [
    { "text": "حاصل عبارت $(x+5)^2$ را با استفاده از اتحاد مربع دو جمله‌ای بسط دهید." },
    { "text": "عبارت $(2a-3b)^2$ را گسترش دهید و جواب را ساده کنید." },
    { "text": "جای خالی را کامل کنید: $$(m+n)^2 = m^2 + \\\\dots + n^2$$" },
    { "text": "درستی تساوی زیر را بررسی کنید: $$(3x+4)^2 = 9x^2 + 24x + 16$$" },
    { "text": "اگر $(x-7)^2 = x^2 - 14x + k$ باشد، مقدار $k$ را بیابید." }
  ]
}`;
}
