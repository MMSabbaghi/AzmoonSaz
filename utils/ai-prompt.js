function getAIPrompt(topic, count) {
  return (
    "لطفاً دقیقا " +
    count +
    ' سوال ریاضی با موضوع "' +
    topic +
    '" تولید کن.\n\n' +
    "الزامات خروجی:\n\n" +
    "1. هر آیتم باید یک سوال واقعی و قابل حل باشد. هیچ متن اضافه، تعریف درس یا توضیح تولید نشود.\n" +
    "2. خروجی فقط یک شیء JSON معتبر باشد.\n" +
    "3. ساختار JSON:\n" +
    "{\n" +
    '  "items": [\n' +
    '    { "text": "متن سوال" }\n' +
    "  ]\n" +
    "}\n" +
    "4. تعداد آیتم‌ها دقیقا " +
    count +
    ' باشد و هر آیتم فقط کلید "text" داشته باشد.\n' +
    "5. فرمول‌ها با $...$ یا $$...$$ باشند.\n" +
    "6. همه بک‌اسلش‌های LaTeX دوبل شوند (مثلاً \\\\frac, \\\\dots, \\\\sqrt).\n" +
    "7. کاراکترهای خاص (^, _, {, }, (, )) نیازی به escape ندارند مگر در رشته JSON.\n" +
    "8. هیچ کاما یا کاراکتر اضافی در انتهای آرایه نباشد.\n" +
    "9. خروجی باید مستقیم با JSON.parse قابل استفاده و KaTeX-ready باشد.\n" +
    "10. مطمئن شو همه سوال‌ها واقعی و قابل حل باشند.\n\n" +
    "نمونه خروجی صحیح:\n" +
    "{\n" +
    '  "items": [\n' +
    '    { "text": "حاصل عبارت $(x+5)^2$ را با استفاده از اتحاد مربع دو جمله‌ای بسط دهید." },\n' +
    '    { "text": "عبارت $(2a-3b)^2$ را گسترش دهید و جواب را ساده کنید." },\n' +
    '    { "text": "جای خالی را کامل کنید: $$(m+n)^2 = m^2 + \\\\dots + n^2$$" }\n' +
    "  ]\n" +
    "}\n\n" +
    "نمونه خروجی نادرست:\n" +
    "{\n" +
    '  "items": [\n' +
    '    { "text": "حاصل عبارت $(x+5)^2$ را با استفاده از اتحاد مربع دو جمله‌ای بسط دهید." },\n' +
    '    { "text": "جای خالی را کامل کنید: $$(m+n)^2 = m^2 + \\dots + n^2$$" },\n' +
    '    { "text": "درستی تساوی زیر را بررسی کنید: $$(3x+4)^2 = 9x^2 + 24x + 16$$," }\n' +
    "  ]\n" +
    "}\n\n" +
    "توضیح:\n" +
    "- در نمونه نادرست، \\\\dots بک‌اسلشش دوبل نشده است.\n" +
    "- در یکی از موارد، یک کاما اضافی بعد از آخرین عنصر آرایه وجود دارد که JSON را نامعتبر می‌کند.\n" +
    "- این باعث می‌شود KaTeX نتواند فرمول‌ها را رندر کند."
  );
}
